    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) throws FileNotFoundException, IOException {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
    	if(getMode().equals(READ_MODE)){
    		leaseTextPane.setText(null);
    	}
    	if(getMode().equals(WRITE_MODE))
    	{
    		leaseTextPane.setText(null);
    	    leaseTextPane.read( leaseProcessor.read(), "lease" );                   
    	    leaseProcessor.read().close();   
    	    setReadMode();
    	}
        
    }//GEN-LAST:event_deleteButtonActionPerformed

