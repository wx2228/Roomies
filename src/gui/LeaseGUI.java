    private void setReadMode(){ 
    	leaseTextPane.setEditable(false);
    	editButton.setText("Edit");
    	deleteButton.setText("Delete");
    	mode = 0;
    }
    
    private void setWriteMode(){ 
    	leaseTextPane.setEditable(true);
    	editButton.setText("Save");
    	deleteButton.setText("Discard");
    	mode = 1;
    }
    
    private String getMode(){
    	if(mode == 0 && editButton.getText().equals("Edit")&&deleteButton.getText().equals("Delete")){
    		return READ_MODE;
    	}
    	else return WRITE_MODE;
    }

