    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) throws IOException {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        if(getMode().equals(READ_MODE)){
        	setWriteMode();
        }
        else if (getMode().equals(WRITE_MODE)){
        	// now this event equals to click "save" button
        	leaseProcessor.write().write(leaseTextPane.getText());;
        	leaseProcessor.write().close();
        	setReadMode();
    }
            
    }//GEN-LAST:event_editButtonActionPerformed

