    private void addBillButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addBillButtonActionPerformed
        // TODO add your handling code here:
        if(roommate1Label.isSelected()||roommate2Label.isSelected()||roommate3Label.isSelected())
        {}
        else 
        {
             Wrong wrongFormat = new Wrong();
             wrongFormat.setText("At lease one person,please");
             wrongFormat.setVisible(true);
             return;
        }
        String aText = amountText.getText();
        int dotFlag =0;
      
        for(int i=aText.length();--i>=0;)
        {
            if(Character.isDigit(aText.charAt(i)))
                continue;
            else  
            {    String s = ""+aText.charAt(i);
                if(dotFlag<2&&s.equals(".")&&i>=1&&i<aText.length()-1)
                {
                        dotFlag++;
                }
                else
                {
                     Wrong wrongFormat = new Wrong();
                     wrongFormat.setText("Digit only, please");
                     wrongFormat.setVisible(true);
                     return;
                }
               
            }
        }
        int peopleIndex=0;
        if(roommate1Label.isSelected())
            peopleIndex=peopleIndex+4;
        if(roommate2Label.isSelected())
            peopleIndex=peopleIndex+2;
        if(roommate3Label.isSelected())
            peopleIndex=peopleIndex+1;
        
        if(DescriptionText.getText().equals("Type description here")&&DescriptionText.getForeground()!=Color.black)
        {DescriptionText.setText("");}
        BillData newBill = new BillData(monthComboBox.getSelectedIndex()+1,dayComboBox.getSelectedIndex()+1,yearComboBox.getSelectedIndex()+2000,peopleIndex,amountText.getText(),DescriptionText.getText());
        ExistingBill.addBill(newBill);
        BillCheckGUI billGUI = new BillCheckGUI();
        billGUI.billLoading();
        billGUI.setVisible(true);
        this.dispose();
    }//GEN-LAST:event_addBillButtonActionPerformed

